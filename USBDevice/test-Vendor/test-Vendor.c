//--------------------------------------------------------------------+
// USB CDC
//--------------------------------------------------------------------+
void cdc_task(void)
{
  // connected() check for DTR bit
  // Most but not all terminal client set this when making connection
  if ( tud_cdc_connected() )
  {
    // connected and there are data available
    if ( tud_cdc_available() )
    {
      // read data
      char buf[64];
      uint32_t count = tud_cdc_read(buf, sizeof(buf));
      
      if (count > 0)
      {
        // Echo back the data with additional message
        tud_cdc_write(buf, count);
        
        // Add indication that this is from CDC class
        if (buf[count-1] == '\r' || buf[count-1] == '\n')
        {
          tud_cdc_write(" [CDC Device]\r\n", 15);
        }
        
        tud_cdc_write_flush();
      }
    }
  }
  else
  {
    // Send periodic message when not connected
    static uint32_t last_msg = 0;
    if (board_millis() - last_msg > 5000) // Every 5 seconds
    {
      last_msg = board_millis();
      tud_cdc_write("CDC Device Ready\r\n", 18);
      tud_cdc_write_flush();
    }
  }
}

// Invoked when cdc when line state changed e.g connected/disconnected
void tud_cdc_line_state_cb(uint8_t itf, bool dtr, bool rts)
{
  (void) itf;
  (void) rts;

  // Terminal connection status based on DTR
  if ( dtr )
  {
    // Terminal connected
    tud_cdc_write_str("Terminal Connected\r\n");
  }else
  {
    // Terminal disconnected
  }
}

// Invoked when CDC interface received data from host
void tud_cdc_rx_cb(uint8_t itf)
{
  (void) itf;
}

//--------------------------------------------------------------------+
// BLINKING TASK
//--------------------------------------------------------------------+
void led_blinking_task(void)
{
  static uint32_t start_ms = 0;
  static bool led_state = false;

  // Blink every interval ms
  if ( board_millis() - start_ms < blink_interval_ms) return; // not enough time
  start_ms += blink_interval_ms;

  board_led_write(led_state);
  led_state = 1 - led_state; // toggle
}

